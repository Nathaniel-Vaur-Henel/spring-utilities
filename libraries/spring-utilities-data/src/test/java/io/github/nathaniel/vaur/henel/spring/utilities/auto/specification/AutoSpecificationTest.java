    @Autowired
    private TestEntityRepository repository;

    @BeforeEach
    void setUp() {
        repository.deleteAll();
        repository.saveAllAndFlush(TestEntityBuilder.build(TEST_ENTITY_COUNT));
    }

    @Nested
    class OverSearch {
        @Test
        void using_over_search_should_ignore_all_other_params() {
            // given
            String value = "string_0";
            Map<TestEntityRequestParamType, String> params = new HashMap<>();
            params.put(GLOBAL_EQUAL, value);
            params.put(NUMBER_EQUAL, "5");
            params.put(STRING_EQUAL, "string_1");
            AutoSpecification<TestEntityRequestParamType, TestEntity> specification =
                    new AutoSpecification<>(TestEntityRequestParamType.values(), params);

            // when
            var result = repository.findAll(specification);

            // then
            Assertions.assertThat(result)
                    .hasSize(1)
                    .element(0)
                    .extracting(TestEntity::getString)
                    .isEqualTo(value);
        }
    }

    @Nested
    class OverSearchEqual {
        @Test
        void using_over_search_with_equal_should_build_correct_predicate_in_autoSpecification() {
            // given
            String value = "string_0";
            var specification = buildSimpleAutoSpecification(TestEntityRequestParamType.GLOBAL_EQUAL, value);

            // when
            var result = repository.findAll(specification);

            // then
            Assertions.assertThat(result)
                    .hasSize(1)
                    .element(0)
                    .extracting(TestEntity::getString)
                    .isEqualTo(value);
        }

        @Test
        void using_over_search_with_equal_should_not_found_exclude_params() {
            // given
            String value = "10";
            var specification = buildSimpleAutoSpecification(GLOBAL_EQUAL, value);

            // when
            var result = repository.findAll(specification);

            // then
            Assertions.assertThat(result).isEmpty();
        }
    }

